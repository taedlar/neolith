TEST_F(CryptTest, MD5FormatDetection) {
    // Test MD5 format detection via $1$ prefix
    char* result = crypt("password", "$1$abcd1234$");
    ASSERT_NE(result, nullptr);
    // Result should contain $1$ prefix for MD5
    EXPECT_TRUE(strstr(result, "$1$") != nullptr);
}

TEST_F(CryptTest, MD5SaltExtraction) {
    // Test that MD5 correctly extracts salt from $1$<salt>$ format
    const char* password = "testpass";
    
    // Hash with explicit MD5 salt format
    char* result1 = crypt(password, "$1$mysalt$");
    ASSERT_NE(result1, nullptr);
    std::string hash1(result1);
    
    // Hash again with same salt - should be identical
    char* result2 = crypt(password, "$1$mysalt$");
    ASSERT_NE(result2, nullptr);
    std::string hash2(result2);
    
    // Both should have same format and be consistent
    EXPECT_NE(hash1.find("$1$"), std::string::npos);
    EXPECT_EQ(hash1, hash2);
}

