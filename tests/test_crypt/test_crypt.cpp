#include <gtest/gtest.h>
#include <cstring>
#include <cstdlib>

extern "C" char* crypt(const char* key, const char* salt);

class CryptTest : public ::testing::Test {
protected:
    void SetUp() override {
        // Seed rand for reproducible salt generation
        srand(42);
    }
};

TEST_F(CryptTest, NonNullResult) {
    char* result = crypt("password", "ab");
    ASSERT_NE(result, nullptr);
    EXPECT_GT(strlen(result), 0);
}

TEST_F(CryptTest, SaltIncludedInResult) {
    char* result = crypt("password", "mysalt");
    ASSERT_NE(result, nullptr);
    // Result should contain the salt
    EXPECT_NE(strstr(result, "mysalt"), nullptr);
}

TEST_F(CryptTest, ConsistentHashingWithSameSalt) {
    const char* password = "testpassword";
    const char* salt = "ab";
    
    char* result1 = crypt(password, salt);
    ASSERT_NE(result1, nullptr);
    std::string hash1(result1);
    
    char* result2 = crypt(password, salt);
    ASSERT_NE(result2, nullptr);
    std::string hash2(result2);
    
    // Same password and salt should produce same hash
    EXPECT_EQ(hash1, hash2);
}

TEST_F(CryptTest, DifferentPasswordsDifferentHashes) {
    const char* salt = "ab";
    
    char* result1 = crypt("password1", salt);
    ASSERT_NE(result1, nullptr);
    std::string hash1(result1);
    
    char* result2 = crypt("password2", salt);
    ASSERT_NE(result2, nullptr);
    std::string hash2(result2);
    
    // Different passwords should produce different hashes
    EXPECT_NE(hash1, hash2);
}

