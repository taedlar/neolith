TEST_F(CryptTest, MD5OldPasswordCompatibility) {
    // Test compatibility with old MD5 hashes
    const char* password = "mypassword";
    
    // First hash: generate MD5 with a known salt
    char salt_str[] = "$1$oldsalt$";
    char* hash = crypt(password, salt_str);
    ASSERT_NE(hash, nullptr);
    
    // Verify it's in MD5 format
    EXPECT_NE(strstr(hash, "$1$"), nullptr);
    EXPECT_NE(strstr(hash, "oldsalt"), nullptr);
    
    // Second call with same password and salt should produce same hash
    char* hash2 = crypt(password, hash);  // Can pass full hash as salt
    ASSERT_NE(hash2, nullptr);
    
    // At minimum, both should be MD5 format
    EXPECT_NE(strstr(hash2, "$1$"), nullptr);
}

