TEST_F(CryptTest, MD5VsSHA256Different) {
    // Test that MD5 and SHA256 produce different outputs
    const char* password = "samepassword";
    const char* salt_text = "ab";
    
    char* md5_result = crypt(password, "$1$ab$");
    ASSERT_NE(md5_result, nullptr);
    std::string md5_hash(md5_result);
    
    char* sha256_result = crypt(password, salt_text);
    ASSERT_NE(sha256_result, nullptr);
    std::string sha256_hash(sha256_result);
    
    // They should be different hashes
    EXPECT_NE(md5_hash, sha256_hash);
}

TEST_F(CryptTest, MD5FormatStructure) {
    // Verify MD5 format: $1$<salt>$<hash>
    char* result = crypt("password", "$1$test$");
    ASSERT_NE(result, nullptr);
    
    // Count dollar signs
    int dollar_count = 0;
    const char* p = result;
    while (*p) {
        if (*p == '$') dollar_count++;
        p++;
    }
    
    // Should have exactly 3 dollar signs
    EXPECT_EQ(dollar_count, 3);
    
    // Should start with $1$
    EXPECT_EQ(result[0], '$');
    EXPECT_EQ(result[1], '1');
    EXPECT_EQ(result[2], '$');
}

TEST_F(CryptTest, SHA256FormatStructure) {
    // Verify SHA256 format has $5$ prefix
    char* result = crypt("password", "$5$test$");
    ASSERT_NE(result, nullptr);
    
    // Should start with $5$
    EXPECT_EQ(result[0], '$');
    EXPECT_EQ(result[1], '5');
    EXPECT_EQ(result[2], '$');
}
